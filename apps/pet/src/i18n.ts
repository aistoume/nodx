export type StringKey = keyof (typeof STRINGS)['en'];

const LANG_KEY = 'nodx-pet-lang';

function detectLocale(): Locale {
  const lang = (typeof navigator !== 'undefined' && navigator.language) || 'en';
  const lower = lang.toLowerCase();
  for (const loc of ['zh', 'fr', 'ja', 'es', 'de', 'ko', 'pt', 'it', 'ru', 'ar'] as const) {
    if (lower.startsWith(loc)) return loc;
  }
  return 'en';
}

export function getLanguage(): Language {
  return (localStorage.getItem(LANG_KEY) as Language | null) ?? 'auto';
}

export function setLanguage(l: Language): void {
  localStorage.setItem(LANG_KEY, l);
}

export function resolveLocale(setting: Language = getLanguage()): Locale {
  return setting === 'auto' ? detectLocale() : setting;
}

/** Translate. Falls back to English when a locale misses a key. */
export function t(key: StringKey): string {
  const loc = resolveLocale();
  return (STRINGS[loc] as Record<string, string>)[key] ?? STRINGS.en[key];
}

/** RTL locales need the document direction flipped. */
export function applyDir(): void {
  if (typeof document === 'undefined') return;
  document.documentElement.dir = resolveLocale() === 'ar' ? 'rtl' : 'ltr';
}
